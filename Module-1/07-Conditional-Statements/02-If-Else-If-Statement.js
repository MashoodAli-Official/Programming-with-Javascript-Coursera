// If else if statement in JavaScript
var marks = 90;
if(marks >= 90) {
    console.log("You have achieved an A grade.");
}
else if(marks >= 75) {
    console.log("You have achieved a B grade.");
}
else if(marks >= 50) { 
    console.log("You have achieved a C grade.");
}
else {
    console.log("You have failed the exam.");
}