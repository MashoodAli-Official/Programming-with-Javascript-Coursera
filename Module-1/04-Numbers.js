// In JavaScript language the numbers are foundational data type because they are represented
// the both integers and decimal values.
// In Javascript we don't have to specify the type of number, it is automatically determined by the value assigned.
console.log(42); // This is an integer
console.log(3.14); // This is a floating point number

//The Javascript is following the sequence of simple mathematical operations
console.log(5 + 3); // Addition
console.log(10 - 2); // Subtraction
console.log(4 * 2); // Multiplication
console.log(8 / 2); // Division
console.log(5 % 2); // Modulus (remainder of division)
console.log(2 ** 3); // Exponentiation (2 raised to the power of 3)

// In javascript we can control the calculation order using parentheses
console.log(2*4+8); // 16
console.log(2*(4+8)) // 24