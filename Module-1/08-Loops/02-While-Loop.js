// In JavaScript, the while loop is a control flow statement that repeatedly executes a block of code as long as a specified condition evaluates to true
var counter = 3;
while (counter > 0) {
    console.log(counter);
    counter = counter - 1; // Decrement the counter by 1
};
console.log("Liftoff!"); // This will execute after the loop ends
// loops are common way to automate the repetitive tasks in programming