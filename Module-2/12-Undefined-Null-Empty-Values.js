// undefined: A variable that has been declared without assigned a value.
let a;
console.log(a); // Output: undefined

// null: A variable that is explicitly set to have no value.
let b = null;
console.log(b); // Output: null

// empty value: An empty string ("") is a value with zero characters.
let c = "";
console.log(c); // Output: (prints nothing, just an empty line)