// Here are the main principles of Object-Oriented Programming (OOP) in JavaScript:

// Encapsulation
// Bundling data (properties) and methods (functions) inside objects. Access to data can be restricted using closures or ES6 classes with private fields.

// Abstraction
// Hiding complex implementation details and exposing only the necessary parts of an object or class.

// Inheritance
// Creating new objects or classes based on existing ones, allowing reuse and extension of functionality. In JavaScript, this is done using prototypes or ES6 class and extends.

// Polymorphism
// Objects or classes can share the same interface but have different implementations. Methods can be overridden in subclasses.